# 9. Manipulação de Listas Aninhadas

# Dada a matriz abaixo (uma lista de listas), como você acessaria o número 8 e o alteraria para o valor 10?

matriz = [
    [1, 2, 3],  # Linha 0
    [4, 5, 6],  # Linha 1
    [7, 8, 9]   # Linha 2
]
matriz[2][1] = 10  # Acessa o número 8 e altera para 10
print(matriz)