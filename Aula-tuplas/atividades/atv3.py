# Exercício 3: Concatenação de Tuplas
# Crie duas tuplas: vogais = ('a', 'e', 'i', 'o', 'u') e numeros = (1, 2, 3).
# Crie uma terceira tupla chamada alfanumerico que seja a combinação das duas tuplas anteriores. Imprima a tupla alfanumerico.

vogais = ('a', 'e', 'i', 'o', 'u')
numeros = (1, 2, 3)
alfanumerico = vogais + numeros
print(f"Tupla concatenada: {alfanumerico}\n")