# Exercício 4: Contando Elementos
# Crie uma tupla com os seguintes elementos: (1, 2, 5, 2, 8, 2, 9, 2).
# Use um método de tupla para descobrir e imprimir quantas vezes o número 2 aparece na tupla.

ocorrencias = (1, 2, 5, 2, 8, 2, 9, 2)
contagem_do_2 = ocorrencias.count(2)
print(f"O número 2 aparece {contagem_do_2} vezes.\n")